int solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int maxReach = a[0];
    int jump = 1;
    int step = a[0];

    if (n == 1)
        return 0;
    if (a[0] == 0)
        return -1;
    else
    {
        for (int i = 1; i < n; i++)
        {
            if (i == n - 1)
                return jump;

            maxReach = max(maxReach, a[i] + i);
            step--;

            if (step == 0)
            {
                jump++;
                if (i >= maxReach)
                {
                    return -1;
                }
                step = maxReach - i;
            }
        }
    }
}

int main()
{
    fast_cin();
    ll t;
    cin >> t;
    for (int it = 1; it <= t; it++)
    {
        int ans = solve();
        cout << ans << "\n";
    }
    return 0;
}